#include <stdio.h>

int main() {

    int a = 21;
    int b = 10;

    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a \% b = %d\n", a % b);
    printf("a++ = %d\n", a++); /* 加一 */
    printf("a-- = %d\n", a--); /* 减一 */

    return 0;
}
